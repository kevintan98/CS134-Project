
public class PokemonMoves {
	public String name;
	public String type;
	public int damage;
	public String attribute;
	public int pp;
	public int accuracy;
	public String priority;
	public PokemonMoves(String name, String type, int damage, String attribute, int pp, int accuracy, String priority){
		this.name = name;
		this.type = type;
		this.damage = damage;
		this.attribute = attribute;
		this.pp = pp;
		this.accuracy = accuracy;
		this.priority = priority;
	}
}
